// ── parsePackageLock (v2/v3) ────────────────────────────

test('parsePackageLock: v3 basic', () => {
  const lock = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': { version: '1.0.0' },
      'node_modules/lodash': { version: '4.17.21' },
      'node_modules/express': { version: '4.18.2' },
    },
  });
  const pkgs = parsePackageLock(lock);
  assert.strictEqual(pkgs.size, 2);
  assert.strictEqual(pkgs.get('lodash').version, '4.17.21');
  assert.strictEqual(pkgs.get('express').version, '4.18.2');
});

test('parsePackageLock: v3 scoped package', () => {
  const lock = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': { version: '1.0.0' },
      'node_modules/@babel/core': { version: '7.20.0' },
    },
  });
  const pkgs = parsePackageLock(lock);
  assert.strictEqual(pkgs.get('@babel/core').version, '7.20.0');
});

test('parsePackageLock: v3 nested dedupes to first', () => {
  const lock = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': {},
      'node_modules/foo': { version: '1.0.0' },
      'node_modules/bar/node_modules/foo': { version: '2.0.0' },
    },
  });
  const pkgs = parsePackageLock(lock);
  assert.strictEqual(pkgs.get('foo').version, '1.0.0');
});

test('parsePackageLock: v3 skips root', () => {
  const lock = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': { version: '1.0.0', name: 'myapp' },
      'node_modules/lodash': { version: '4.17.21' },
    },
  });
  const pkgs = parsePackageLock(lock);
  assert.strictEqual(pkgs.size, 1);
  assert.ok(!pkgs.has('myapp'));
});

