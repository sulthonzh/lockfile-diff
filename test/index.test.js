// ── computeStats ────────────────────────────────────────

test('computeStats: basic', () => {
  const diff = {
    added: [{ name: 'a', version: '1.0.0' }],
    removed: [{ name: 'b', version: '2.0.0' }],
    changed: [
      { name: 'c', before: '1.0.0', after: '1.1.0' },
      { name: 'd', before: '1.0.0', after: '2.0.0' },
    ],
    unchanged: [{ name: 'e', version: '5.0.0' }],
  };
  const stats = computeStats(diff);
  assert.strictEqual(stats.added, 1);
  assert.strictEqual(stats.removed, 1);
  assert.strictEqual(stats.changed, 2);
  assert.strictEqual(stats.unchanged, 1);
  assert.strictEqual(stats.total, 5);
  assert.strictEqual(stats.changeTypes.minor, 1);
  assert.strictEqual(stats.changeTypes.major, 1);
});

test('computeStats: empty diff', () => {
  const stats = computeStats({ added: [], removed: [], changed: [], unchanged: [] });
  assert.strictEqual(stats.total, 0);
});

// ── Integration: parse + diff ───────────────────────────

test('integration: npm lockfile full flow', () => {
  const before = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': {},
      'node_modules/lodash': { version: '4.17.20' },
      'node_modules/express': { version: '4.17.0' },
    },
  });
  const after = JSON.stringify({
    lockfileVersion: 3,
    packages: {
      '': {},
      'node_modules/lodash': { version: '4.17.21' },
      'node_modules/mocha': { version: '10.0.0' },
    },
  });
  const beforeMap = parseLockfile(before, 'npm');
  const afterMap = parseLockfile(after, 'npm');
  const diff = diffLockfiles(beforeMap, afterMap);
  assert.strictEqual(diff.changed.length, 1);
  assert.strictEqual(diff.added.length, 1);
  assert.strictEqual(diff.removed.length, 1);
  assert.strictEqual(diff.changed[0].name, 'lodash');
  assert.strictEqual(diff.added[0].name, 'mocha');
  assert.strictEqual(diff.removed[0].name, 'express');
});

