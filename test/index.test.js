'use strict';

const assert = require('assert');
const {
  parsePackageLock,
  parseYarnLock,
  parsePnpmLock,
  diffLockfiles,
  detectFormat,
  parseLockfile,
  classifyVersionChange,
  computeStats,
  extractPkgName,
  extractYarnPkgName,
} = require('../src/index');

let passed = 0;
let failed = 0;

function test(name, fn) {
  try {
    fn();
    passed++;
    process.stdout.write('.');
  } catch (err) {
    failed++;
    process.stdout.write('F');
    console.error(`\n✗ ${name}: ${err.message}`);
  }
}

